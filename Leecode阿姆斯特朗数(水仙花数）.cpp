//阿姆斯特朗数也就是俗称的水仙花数，是指一个三位数，其各位数字的立方和等于该数本身。
//算法思想具体如下：
//分离出个位数，算术表达式为：j=i%10
//分离出十位数，算术表达式为：k=i/10%10。
//分离出百位数，算术表达式为：n=i/100。
//判断条件是否成立。若是，执行步骤 ⑤；若不是，执行步骤 ⑥。
//打印输出结果。
//i 自增 1
//执行，直到 i 等于 1000。
//其判断的条件为：j*j*j+k*k*k+n*n*n==i

#include <stdio.h>
int main()
{
    int i,j,k,n;
    for(i=100;i<1000;i++)
    {
        j=i%10;
        k=i/10%10;
        n=i/100;
        if(j*j*j+k*k*k+n*n*n==i)
            printf("%5d\n",i);
    }
    return 0;
}
